#Ввод двух чисел
a=int(input('Введите первое число: '))
b=int(input('Введите второе число: '))

#Сумма чисел
res=a+b
print('Сумма равна: ', res)

#Разность чисел
res=a-b
print('Разность равна: ', res)

#Произведение чисел
res=a*b
print('Произведение равно: ', res)

#Частное чисел
res=a//b
print('Частное равно: ', res)
